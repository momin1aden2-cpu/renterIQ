/**
 * RenterIQ — PDF Report Export (RIQExport)
 *
 * Generates branded PDF reports from HTML using html2pdf.js (loaded from CDN).
 * Used for inspection reports, condition reports, cover letters, and lease summaries.
 *
 * Usage:
 *   RIQExport.generate({
 *     title: 'Open Home Inspection Report',
 *     subtitle: '42 Smith St, Fitzroy VIC 3065',
 *     date: new Date(),
 *     sections: [
 *       { heading: 'Living Area', content: 'Good condition...', badge: 'Good' },
 *       ...
 *     ],
 *     filename: 'inspection-report.pdf'
 *   });
 */
(function() {
  'use strict';

  var CDN_URL = 'https://cdnjs.cloudflare.com/ajax/libs/html2pdf.js/0.10.2/html2pdf.bundle.min.js';
  var loaded = false;

  function loadLib(cb) {
    if (loaded && window.html2pdf) { cb(); return; }
    var script = document.createElement('script');
    script.src = CDN_URL;
    script.onload = function() { loaded = true; cb(); };
    script.onerror = function() { cb(new Error('Failed to load PDF library')); };
    document.head.appendChild(script);
  }

  function buildHTML(opts) {
    var now = opts.date || new Date();
    var dateStr = now.toLocaleDateString('en-AU', { day: 'numeric', month: 'long', year: 'numeric' });
    var timeStr = now.toLocaleTimeString('en-AU', { hour: '2-digit', minute: '2-digit' });

    var html = '';
    html += '<div style="font-family:Arial,Helvetica,sans-serif;color:#1A2B4A;padding:0;max-width:700px;margin:0 auto">';

    // Header
    html += '<div style="background:linear-gradient(135deg,#0A2460,#1B50C8);padding:28px 24px;border-radius:0 0 16px 16px;margin-bottom:24px">';
    html += '<div style="font-size:22px;font-weight:800;color:#fff;letter-spacing:-0.3px">' + esc(opts.title || 'RenterIQ Report') + '</div>';
    if (opts.subtitle) {
      html += '<div style="font-size:14px;color:rgba(255,255,255,.7);margin-top:6px;font-weight:600">' + esc(opts.subtitle) + '</div>';
    }
    html += '<div style="font-size:11px;color:rgba(255,255,255,.5);margin-top:10px;font-weight:600">' + dateStr + ' at ' + timeStr + ' · Generated by RenterIQ</div>';
    html += '</div>';

    // Sections
    if (opts.sections && opts.sections.length) {
      opts.sections.forEach(function(sec) {
        html += '<div style="background:#fff;border:1px solid #E8EFF8;border-radius:12px;padding:16px 18px;margin-bottom:12px;page-break-inside:avoid">';
        if (sec.heading) {
          html += '<div style="display:flex;justify-content:space-between;align-items:center;margin-bottom:8px">';
          html += '<div style="font-size:15px;font-weight:700;color:#1A2B4A">' + esc(sec.heading) + '</div>';
          if (sec.badge) {
            var badgeColor = sec.badge === 'Good' ? '#10B981' : sec.badge === 'Issues' ? '#F59E0B' : sec.badge === 'Concerns' ? '#EF4444' : '#6B7B99';
            html += '<span style="background:' + badgeColor + ';color:#fff;padding:3px 10px;border-radius:100px;font-size:10px;font-weight:700">' + esc(sec.badge) + '</span>';
          }
          html += '</div>';
        }
        if (sec.content) {
          html += '<div style="font-size:13px;color:#6B7B99;line-height:1.7;white-space:pre-wrap">' + esc(sec.content) + '</div>';
        }
        if (sec.items && sec.items.length) {
          sec.items.forEach(function(item) {
            html += '<div style="font-size:13px;color:#6B7B99;line-height:1.7;padding:4px 0;border-bottom:1px solid #f0f4fa">' + esc(item) + '</div>';
          });
        }
        if (sec.photos && sec.photos.length) {
          html += '<div style="display:flex;flex-wrap:wrap;gap:8px;margin-top:10px">';
          sec.photos.forEach(function(photo) {
            html += '<img src="' + photo + '" style="width:120px;height:90px;object-fit:cover;border-radius:8px;border:1px solid #E8EFF8">';
          });
          html += '</div>';
        }
        html += '</div>';
      });
    }

    // Free-text body
    if (opts.body) {
      html += '<div style="font-size:13px;color:#1A2B4A;line-height:1.8;white-space:pre-wrap;padding:0 4px">' + esc(opts.body) + '</div>';
    }

    // Red flags
    if (opts.redFlags && opts.redFlags.length) {
      html += '<div style="background:#FEF2F2;border:1px solid rgba(239,68,68,.2);border-radius:12px;padding:16px 18px;margin-bottom:12px;page-break-inside:avoid">';
      html += '<div style="font-size:14px;font-weight:700;color:#991B1B;margin-bottom:8px">Red Flags</div>';
      opts.redFlags.forEach(function(f) {
        html += '<div style="font-size:13px;color:#7F1D1D;padding:4px 0">⚠️ ' + esc(f) + '</div>';
      });
      html += '</div>';
    }

    // Footer
    html += '<div style="text-align:center;padding:20px 0;font-size:10px;color:#9AA4B8;border-top:1px solid #E8EFF8;margin-top:16px">';
    html += 'Generated by RenterIQ · renteriq.com.au · This is a personal record, not a legal document.';
    html += '</div>';

    html += '</div>';
    return html;
  }

  function esc(s) {
    if (!s) return '';
    return String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
  }

  window.RIQExport = {
    generate: function(opts) {
      if (typeof showToast === 'function') showToast('Generating PDF', 'Preparing your report…', '📄');

      loadLib(function(err) {
        if (err) {
          if (typeof showToast === 'function') showToast('Error', 'Could not load PDF library', '⚠️');
          return;
        }

        var container = document.createElement('div');
        container.innerHTML = buildHTML(opts);
        container.style.cssText = 'position:absolute;left:-9999px;top:0;width:700px';
        document.body.appendChild(container);

        var pdfOpts = {
          margin: [8, 8, 8, 8],
          filename: opts.filename || 'renteriq-report.pdf',
          image: { type: 'jpeg', quality: 0.92 },
          html2canvas: { scale: 2, useCORS: true, logging: false },
          jsPDF: { unit: 'mm', format: 'a4', orientation: 'portrait' },
          pagebreak: { mode: ['avoid-all', 'css', 'legacy'] }
        };

        html2pdf().set(pdfOpts).from(container).save().then(function() {
          document.body.removeChild(container);
          if (typeof showToast === 'function') showToast('PDF saved', opts.filename || 'Report downloaded', '✓');
        }).catch(function(e) {
          document.body.removeChild(container);
          console.error('PDF export failed:', e);
          if (typeof showToast === 'function') showToast('Error', 'PDF generation failed', '⚠️');
        });
      });
    }
  };
})();
